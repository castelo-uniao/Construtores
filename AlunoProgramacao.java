
public class AlunoProgramacao 
{
    String nome;
    String escolaridade;
    int matricula;
    double nota;

    public AlunoProgramacao()
    {

    }

    public AlunoProgramacao(String nome, String escolaridade, int matricula, double nota)
    {
        this.nome = nome;
        this.escolaridade = escolaridade;
        this.matricula = matricula;
        this.nota = nota;
    }
}